public class ch1ex8 {
    public static void main(String[] args) {
        double radius = 3.7;
        double perimeter;
        double area;
        double pi = 3.14;

        perimeter = 2 * radius * pi;
        area = radius * radius * pi;

        System.out.println("perimeter =  " + perimeter);
        System.out.println("area =  " + area);

    }
}
